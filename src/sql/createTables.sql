CREATE TABLE TEAM (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY,
	NAME VARCHAR(80) NOT NULL); 
	
CREATE TABLE PLAYER (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1) PRIMARY KEY,
	FIRSTNAME VARCHAR(80) NOT NULL,
	LASTNAME VARCHAR(80) NOT NULL,
	EQUIPE_ID INTEGER,
	FOREIGN KEY(EQUIPE_ID) REFERENCES TEAM(ID)
	); 	